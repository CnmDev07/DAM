    private void UltimoSorteoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UltimoSorteoActionPerformed
        try (BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream("sorteos.txt")))) {
            String linea = null;
            String mensaje = null;
            while ((linea = br.readLine()) != null) {
                mensaje = linea;
            }
            String[] numeros = mensaje.split("-");
            n1.setText(numeros[0]);
            n2.setText(numeros[1]);
            n3.setText(numeros[2]);
            n4.setText(numeros[3]);
            n5.setText(numeros[4]);
            n6.setText(numeros[5]);
            n7.setText(numeros[6]);
            n8.setText(numeros[7]);
        } catch (IOException e) {
            ActivarDialogo();
        }
    }//GEN-LAST:event_UltimoSorteoActionPerformed

