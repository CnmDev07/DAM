    private DefaultListModel leerDatos(){
        DefaultListModel model = new DefaultListModel();
        
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Cartelera", "root", "0719")) {
            String query = "SELECT nombre,apellidos FROM Actores";
            try (Statement stmt = conn.createStatement();
                 ResultSet rs = stmt.executeQuery(query)) {

                
                while (rs.next()) {
                    String nombreCompleto = rs.getString("nombre") + " " + rs.getString("apellidos");
                    model.addElement(nombreCompleto);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        return model;
    }
    
     private void eliminarActor(String actor) {
    String url = "jdbc:mysql://localhost:3306/Cartelera"; // Cambia según tus datos
    String user = "root"; // Cambia según tus datos
    String password = "0719"; // Cambia según tus datos

    try (Connection connection = DriverManager.getConnection(url, user, password);
         PreparedStatement statement = connection.prepareStatement(
             "DELETE FROM Actores WHERE nombre = ?")) {

        
        statement.setString(1, actor);
        
        int filasEliminadas = statement.executeUpdate();
        
        if (filasEliminadas > 0) {
            JOptionPane.showMessageDialog(null, "Actor eliminado exitosamente.");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el Actor seleccionado.");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al eliminar el Actor: " + ex.getMessage());
    }
}
    
    

