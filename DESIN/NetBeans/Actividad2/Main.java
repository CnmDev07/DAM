    private void GenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerarActionPerformed

        List<Integer> combinacion = new ArrayList<>();
        Random random = new Random();
        int reintegro;

        while (combinacion.size() < 7) {
            int numero = random.nextInt(49) + 1;
            if (!combinacion.contains(numero)) {
                combinacion.add(numero);
            }
        }
        reintegro = random.nextInt(9) + 1;
        n1.setText(combinacion.get(0) + "");
        n2.setText(combinacion.get(1) + "");
        n3.setText(combinacion.get(2) + "");
        n4.setText(combinacion.get(3) + "");
        n5.setText(combinacion.get(4) + "");
        n6.setText(combinacion.get(5) + "");
        n7.setText(combinacion.get(6) + "");
        n8.setText(reintegro + "");
    }//GEN-LAST:event_GenerarActionPerformed

    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try (BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(new FileOutputStream("sorteos.txt", true)))) {
            bw.write(n1.getText() + "-" + n2.getText() + "-" + n3.getText() + "-" + n4.getText() + "-"
                    + n5.getText() + "-" + n6.getText() + "-" + n7.getText() + "-" + n8.getText() + "\n");
        } catch (IOException e) {
            ActivarDialogo();
        }
    }//GEN-LAST:event_GuardarActionPerformed

