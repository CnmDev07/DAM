    private void Listado_actoresValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_Listado_actoresValueChanged
        int index = Listado_actores.getSelectedIndex();
       String nombreCompleto = (String) model.getElementAt(index);
       String[] nombreApellido = nombreCompleto.split(" ");
        
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Cartelera", "root", "0719")) {
            String query = "SELECT * FROM Actores WHERE nombre = '" + nombreApellido[0] + "' AND apellidos = '" + nombreApellido[1] + "'";
            try (Statement stmt = conn.createStatement();
                 ResultSet rs = stmt.executeQuery(query)) {

                // Llenar el modelo con los resultados de la consulta
                while (rs.next()) {
                    lID.setText(rs.getString("id"));
                    lNombre.setText(rs.getString("nombre"));
                    lApellidos.setText(rs.getString("apellidos"));
                    lFecha.setText(rs.getString("fecha_nacimiento"));
                    lNacionalidad.setText(rs.getString("nacionalidad"));
                    lEspecialidad.setText(rs.getString("genero_especialidad"));
                    lCache.setText(rs.getString("cache"));
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_Listado_actoresValueChanged

